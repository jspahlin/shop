package com.revature.beans;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.SequenceGenerator;
import javax.persistence.Table;

@Entity
@Table(name="card_rarity")
public class CardRarity {
	@Id
	@Column
	@SequenceGenerator(name="card_rarity_gen", sequenceName="card_rarity_seq", allocationSize=1)
    @GeneratedValue(generator="card_rarity_gen", strategy=GenerationType.SEQUENCE)
	private int id;
	private String name;
	public CardRarity() {
		super();
		// TODO Auto-generated constructor stub
	}
	public CardRarity(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + id;
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		CardRarity other = (CardRarity) obj;
		if (id != other.id)
			return false;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		return true;
	}
	@Override
	public String toString() {
		return "CardRarity [id=" + id + ", name=" + name + "]";
	}
	
	
}
